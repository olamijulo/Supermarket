package sample;

public class supplierModal {

    String supplierID, supplierName, phoneNumber, address, email;

    public supplierModal(String supplierID, String supplierName, String phoneNumber,String  address, String email)
    {
        this.supplierID = supplierID;
        this.supplierName = supplierName;
        this.phoneNumber = phoneNumber;
        this.address = address;
        this.email = email;
    }

    public String getSupplierID()
    {
        return supplierID;
    }
    public String setSupplierID()
    {
        return supplierID;
    }
    public String getSupplierName()
    {
        return supplierName;
    }
    public String setSupplierName()
    {
        return supplierName;
    }
    public String getPhoneNumber()
    {
        return phoneNumber;
    }
    public String setPhoneNumber()
    {
        return phoneNumber;
    }
    public String getAddress()
    {
        return address;
    }
    public String setAddress()
    {
        return address;
    }
    public String getEmail()
    {
        return email;
    }
    public String setEmail() { return email; }

}
