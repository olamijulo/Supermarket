package sample;

public class employeeModal {

    String empID, firstName, middleName, lastName, age, dob, phone, address, city;

    public employeeModal(String empID, String firstName, String middleName,String  lastName, String age, String dob, String phone, String address, String city)
    {
        this.empID = empID;
        this.firstName = firstName;
        this.middleName = middleName;
        this.lastName = lastName;
        this.age = age;
        this.dob = dob;
        this.phone = phone;
        this.address = address;
        this.city = city;
    }

    public String getEmpID()
    {
        return empID;
    }
    public String setEmpID()
    {
        return empID;
    }
    public String getFirstName()
    {
        return firstName;
    }
    public String setFirstName()
    {
        return firstName;
    }
    public String getMiddleName()
    {
        return middleName;
    }
    public String setMiddleName()
    {
        return middleName;
    }
    public String getLastName()
    {
        return lastName;
    }
    public String setLastName()
    {
        return lastName;
    }
    public String getAge()
    {
        return age;
    }
    public String setAge() { return age; }
    public String getDob()
    {
        return dob;
    }
    public String setDob()
    {
        return dob;
    }
    public String getPhone()
    {
        return phone;
    }
    public String setPhone()
    {
        return phone;
    }
    public String getAddress()
    {
        return address;
    }
    public String setAddress()
    {
        return address;
    }
    public String getCity()
    {
        return city;
    }
    public String setCity()
    {
        return city;
    }


}
